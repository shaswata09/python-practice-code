print('Logical operator examples are here...')
# and operator... there is no other and expressions like "&&, AND"
print('and logic...')
print('True and True (True):', True and True)
print('True and False (False):', True and False)
print('False and False (False):', False and False)

# or operator... there is no other and expressions like "||, OR"
print('or logic...')
print('True or True (True):', True or True)
print('True or False (True):', True and False)
print('False or False (False):', False and False)

# not operator... there is no other and expressions like "!, NOT"
print('not logic...')
print('not(True) (False):', not True)
print('not(False) (True):', not False)
